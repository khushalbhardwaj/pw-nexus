/**
 * pw-auth-setup.js
 * Saves your PW auth token to pw-token.json for use by proxy.js
 *
 * HOW TO GET YOUR TOKEN:
 * 1. Open https://www.pw.live in Chrome and log in
 * 2. Press F12 → go to "Application" tab → "Local Storage" → click "https://www.pw.live"
 * 3. Find the key named "token"  (or search for it)
 * 4. Copy the long value (starts with "eyJ...")
 * 5. Run this script and paste it when asked
 *
 * Or run with --token flag:
 *    node pw-auth-setup.js --token=eyJ...
 */

const fs   = require('fs');
const path = require('path');
const readline = require('readline');

const TOKEN_FILE = path.join(__dirname, 'pw-token.json');

// Support --token=<value> flag
const tokenArg = process.argv.find(a => a.startsWith('--token='));
if (tokenArg) {
    const token = tokenArg.replace('--token=', '').trim();
    saveToken(token);
    process.exit(0);
}

// Interactive mode
const rl = readline.createInterface({ input: process.stdin, output: process.stdout });

console.log('\n🔐 Physics Wallah Token Setup\n');
console.log('How to get your token (takes ~30 seconds):');
console.log('  1. Open https://www.pw.live in Chrome and LOG IN');
console.log('  2. Press F12 → Application tab → Local Storage → https://www.pw.live');
console.log('  3. Find the key named "token"');
console.log('  4. Copy the long value (starts with "eyJ...")');
console.log('  5. Paste it below\n');

rl.question('Paste your PW token here: ', (token) => {
    rl.close();
    token = token.trim();
    if (!token || token.length < 20) {
        console.error('❌ Token looks too short. Please paste the full token.');
        process.exit(1);
    }
    saveToken(token);
});

function saveToken(token) {
    const config = {
        token,
        savedAt: new Date().toISOString(),
        note: 'Generated by pw-auth-setup.js — restart proxy.js to apply'
    };
    fs.writeFileSync(TOKEN_FILE, JSON.stringify(config, null, 2));
    console.log('\n✅ Token saved to pw-token.json');
    console.log('👉 Now restart proxy: node proxy.js\n');
}
